    public void selectUsers() {

        var listUsers = crudUsers.select();
        model.setRowCount(0);
        for (int i = 0; i < listUsers.size(); i++) {
            var userList = listUsers.get(i);
            Object[] obj = {userList.getId(), userList.getName(), userList.getUsername(), userList.getPassword(), userList.getGender()};
            model.addRow(obj);

        }

    }

    public void clearField() {
        name.setText("");
        username.setText("");
        password.setText("");
        gender.setSelectedItem("Pria");
    }

