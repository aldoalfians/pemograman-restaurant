    private void btnUserDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUserDeleteMouseClicked
        // TODO add your handling code here:
        int tableIndex = usersTable.getSelectedRow();
        int user_id = (int) model.getValueAt(tableIndex, 0);

        crudUsers.delete(user_id);
        selectUsers();
        clearField();
    }//GEN-LAST:event_btnUserDeleteMouseClicked

    private void btnUserEditMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUserEditMouseClicked
        // TODO add your handling code here:
        int tableIndex = usersTable.getSelectedRow();
        int user_id = (int) model.getValueAt(tableIndex, 0);

        if (name.getText().isEmpty() || username.getText().isEmpty() || password.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong");
            return;
        };

        String genderStr = gender.getSelectedItem().toString();
        crudUsers.update(user_id, name.getText(), username.getText(), password.getText(), genderStr);
        selectUsers();
        clearField();
    }//GEN-LAST:event_btnUserEditMouseClicked

