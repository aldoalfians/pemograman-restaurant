    private void drinkTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_drinkTableMouseClicked
        // TODO add your handling code here:
        int tableIndex = drinkTable.getSelectedRow();
        
        String stock = drinkTable.getValueAt(tableIndex, 2).toString();
        
        if ("Kosong".equals(stock)) {
            JOptionPane.showMessageDialog(this, "Stok saat ini Kosong");
        } else {
            name.setText(modelDrink.getValueAt(tableIndex, 1).toString());
            price.setText(modelDrink.getValueAt(tableIndex, 3).toString());
        }
        
        foodTable.clearSelection();
        

    }//GEN-LAST:event_drinkTableMouseClicked

