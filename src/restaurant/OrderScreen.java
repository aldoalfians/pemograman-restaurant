    public void selectFoods() {
        var listFood = crudFood.select();
        modelFood.setRowCount(0);
        for (int i = 0; i < listFood.size(); i++) {
            var foodList = listFood.get(i);
            Object[] obj = {foodList.getKode_food(), foodList.getName(), foodList.getStock(), foodList.getPrice(), foodList.getCategory()};
            modelFood.addRow(obj);
        }
    }
    
    public void selectDrinks() {
        var listDrinks = crudDrink.select();
        modelDrink.setRowCount(0);
        for (int i = 0; i < listDrinks.size(); i++) {
            var drinkList = listDrinks.get(i);
            Object[] obj = {drinkList.getKode_drink(), drinkList.getName(), drinkList.getStock(), drinkList.getPrice(), drinkList.getCategory()};
            modelDrink.addRow(obj);
        }
    }

