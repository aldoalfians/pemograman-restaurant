    private void addOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrderActionPerformed
        // TODO add your handling code here:
        int qtyParse = Integer.parseInt(qty.getText());
        int priceParse = Integer.parseInt(price.getText());
        String orderStatus = "Menunggu Pembayaran";
        System.out.println(username.getText());
        System.out.println(name.getText());
        System.out.println(qty.getText());
        System.out.println(price.getText());
        
        if (orderId.getText().isEmpty() || username.getText().isEmpty() || name.getText().isEmpty() || qty.getText().isEmpty() || price.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data tidak boleh kosong");
            return;
        }
        
        if (checkUsername.isSelected()) {
            crudOrder.insert(orderId.getText(), username.getText(), name.getText(), qtyParse, priceParse, orderStatus);
            
        } else {
            JOptionPane.showMessageDialog(this, "Username silahkan cek terlebih dahulu");
        }
        

    }//GEN-LAST:event_addOrderActionPerformed

